    private void btnLuuTacGiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuTacGiaActionPerformed

        if (coThem) {
            try {
                String tenTacGia = txtTenTacGia.getText();
                String ngaySinh = txtNgaySinh.getText();
                String diaChi = txtDiaChiTacGia.getText();
                TacGiaModel tacGiaModel = new TacGiaModel();
                tacGiaModel.setTenTacGia(tenTacGia);
                tacGiaModel.setNgaySinh(ngaySinh);
                tacGiaModel.setDiaChi(diaChi);
                TacGiaController tacGiaController = new TacGiaController(tacGiaModel, this);
                tacGiaController.SetDataToDB();
                if (tacGiaController.CheckIsNullTacGia()) {
                    this.setButton(false, true);
                    this.setTextField(false);
                } else {
                    this.setButton(true, false);
                    this.setTextField(false);
                    txtTenTacGia.setText("");
                    txtNgaySinh.setText("");
                    txtDiaChiTacGia.setText("");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Lỗi lưu dữ liệu: " + ex.getMessage(), "Lỗi Database", JOptionPane.ERROR_MESSAGE);
                this.setButton(false, true);
            }
        } else if (coThem == false) {
            String tenTacGia = txtTenTacGia.getText();
            String ngaySinh = txtNgaySinh.getText();
            String diaChi = txtDiaChiTacGia.getText();
            TacGiaModel tacGiaModel = new TacGiaModel();
            tacGiaModel.setTenTacGia(tenTacGia);
            tacGiaModel.setNgaySinh(ngaySinh);
            tacGiaModel.setDiaChi(diaChi);
            int pos = this.tblTacGia.getSelectedRow() + 1;
            tacGiaModel.setIdTacGia(pos);
            try {
                TacGiaController tacGiaController = new TacGiaController(tacGiaModel, this);
                tacGiaController.UpdateDataToDB();
                if (tacGiaController.CheckIsNullTacGia()) {
                    this.setButton(false, true);
                    this.setTextField(false);
                } else {
                    this.setButton(true, false);
                    this.setTextField(false);
                }
            } catch (SQLException ex) {
                System.getLogger(ChucNangCuaAdmin.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }

        }
    }//GEN-LAST:event_btnLuuTacGiaActionPerformed
