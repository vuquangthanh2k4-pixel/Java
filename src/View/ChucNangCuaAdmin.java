    public void AddRowToTable(List<TacGiaModel> list){
        DefaultTableModel model = (DefaultTableModel)tblTacGia.getModel();
        model.setNumRows(0);
        for(TacGiaModel i : list){
             System.out.println(i.getIdTacGia() + " - " + i.getTenTacGia());
            model.addRow(new Object[]{
                i.getIdTacGia(),
                i.getTenTacGia(),
                i.getNgaySinh(),
                i.getDiaChi()
            });
        }
        this.list = list;
        System.out.println("Số dòng thêm vào bảng la: " + list.size());
    }
    
