    private void btnXoaTacGiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaTacGiaActionPerformed

        this.setTextField(false);

        String tenTacGia = txtTenTacGia.getText();
        String ngaySinh = txtNgaySinh.getText();
        String diaChi = txtDiaChiTacGia.getText();
        TacGiaModel tacGiaModel = new TacGiaModel();
        tacGiaModel.setTenTacGia(tenTacGia);
        tacGiaModel.setNgaySinh(ngaySinh);
        tacGiaModel.setDiaChi(diaChi);

        TacGiaController tacGiaController = new TacGiaController(tacGiaModel, this);
        if (tacGiaController.CheckIsNullTacGia()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một tác giả để xoá", "Thông báo", 1);
        } else if (JOptionPane.showConfirmDialog(this, "Bạn chắc chắn xoá không?", "Thông báo", 1) == JOptionPane.YES_OPTION) {
            try {
                tacGiaController.DeleteDataToDB();
                GetDataTacGiaDAO getDataTacGiaDAO = new GetDataTacGiaDAO();
                if(!getDataTacGiaDAO.CheckForiegnKeyTacGiaToSach(tenTacGia, ngaySinh, diaChi)){
                    return;
                }
                JOptionPane.showMessageDialog(this, "Xoá thành công tác giả", "Thông báo", 1);
                txtTenTacGia.setText("");
                txtNgaySinh.setText("");
                txtDiaChiTacGia.setText("");

            } catch (SQLException ex) {
                System.out.println("Lỗi không thể xoá: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnXoaTacGiaActionPerformed
    
